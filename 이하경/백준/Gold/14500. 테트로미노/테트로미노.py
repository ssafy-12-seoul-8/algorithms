          tet(m - 1, n - 2, ((1, 0), (1, 1), (0, 1), (0, 2))), tet(m - 2, n - 1, ((0, 1), (1, 1), (1, 0), (2, 0))),
          tet(m - 2, n - 1, ((0, 0), (1, 0), (1, 1), (2, 1))), tet(m - 2, n - 1, ((0, 0), (1, 0), (2, 0), (1, 1))),
          tet(m - 2, n - 1, ((0, 1), (1, 1), (2, 1), (1, 0))), tet(m - 1, n - 2, ((1, 0), (1, 1), (1, 2), (0, 1))),
          tet(m - 1, n - 2, ((0, 0), (0, 1), (0, 2), (1, 1))), tet(m - 1, n - 2, ((0, 0), (0, 1), (0, 2), (1, 2))),
          tet(m - 1, n - 2, ((0, 0), (0, 1), (0, 2), (1, 0))), tet(m - 1, n - 2, ((1, 0), (1, 1), (1, 2), (0, 2))),
